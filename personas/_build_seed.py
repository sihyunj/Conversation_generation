def build_topics(personas: list[dict]) -> list[dict]:
    out = []
    for row in TOPICS_RAW:
        a_idx, b_idx, rel, platform, topic, tone, opener = row
        a = personas[a_idx - 1]
        b = personas[b_idx - 1]
        rec = {
            "topic_id": gen_uuid(),
            "persona_a_uuid": a["uuid"],
            "persona_b_uuid": b["uuid"],
            "persona_a_name": f"{a['first_name']} {a['last_name']}",
            "persona_b_name": f"{b['first_name']} {b['last_name']}",
            "relationship": rel,
            "platform": platform,
            "topic": topic,
            "tone": tone,
            "opener_hint": opener,
        }
        out.append(rec)
    return out


